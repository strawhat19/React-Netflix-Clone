// Sort Object by Key Length
export const sortObjByKeyLength = (obj) => {
  let object = {};
  let keyArray = Object.keys(obj);
  keyArray.sort();
  keyArray.forEach(function (item) {
    object[item] = obj[item];
  });
  return object;
};

export const findSumOfArray = (arrayOfNumbers) => {
  let sum = 0;
  for (let i = 0; i < arrayOfNumbers.length; i++) {
    sum += arrayOfNumbers[i];
  }
  return sum;
};

export const calcHoursAndCost = (course) => {
  let projectHours = [];
  let projectCost = [];

  course.projects.forEach((project) => {
    projectHours.push(project.approximateHours);
    projectCost.push(project.approximateCost);

    let hours = findSumOfArray(projectHours);
    let cost = findSumOfArray(projectCost);

    localStorage.setItem(`${course.level} Hours`, hours);
    localStorage.setItem(`${course.level} Cost`, cost);
  });

  console.log(
    `${course.level} Hours`,
    localStorage.getItem(`${course.level} Hours`)
  );
  console.log(
    `${course.level} Cost`,
    localStorage.getItem(`${course.level} Cost`)
  );
};

